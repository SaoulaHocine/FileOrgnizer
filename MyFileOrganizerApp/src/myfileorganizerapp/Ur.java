package myfileorganizerapp;

public class Ur {

    private int ID;
    private String Path;

    public Ur(int ID, String Path) {

        this.ID = ID;
        this.Path = Path;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getPath() {
        return Path;
    }

    public void setPath(String Path) {
        this.Path = Path;
    }

}
